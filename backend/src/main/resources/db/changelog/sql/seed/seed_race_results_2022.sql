SELECT gen_random_uuid(), d.race_date, NULL
FROM (SELECT DISTINCT race_date FROM tmp_results_2022) d
WHERE NOT EXISTS (SELECT 1 FROM race r WHERE r.race_date = d.race_date);

-- 2. Create runners whose name is not yet in the runner table.
INSERT INTO runner (uuid, name, gender)
SELECT gen_random_uuid(), t.name, t.gender
FROM (SELECT DISTINCT ON (name) name, gender FROM tmp_results_2022 ORDER BY name, gender) t
WHERE NOT EXISTS (SELECT 1 FROM runner r WHERE r.name = t.name);

-- 3. Link runners to races. One runner uuid per name; skip duplicate entries in the same race.
INSERT INTO race_runner (runner_uuid, race_uuid, result_time, hide_time)
SELECT ru.uuid, ra.uuid, s.result_time, s.hide_time
FROM tmp_results_2022 s
JOIN (SELECT DISTINCT ON (name) name, uuid FROM runner ORDER BY name, uuid) ru ON ru.name = s.name
JOIN race ra ON ra.race_date = s.race_date
ON CONFLICT (runner_uuid, race_uuid) DO NOTHING;

